""" Looping/ Iterators """

"""
   Looping or Iterators are used to execute a block of code
   until a condition is satisfied.
"""

""" While Loop """
number = 1

while number <= 5:
	#print(number)
	number = number+1

"""
	While the condition is True, the code inside the block will be executed until condition get False.
	So, the code will print number from 1 to 5

	1
	2
	3
	4
	5
"""


""" For Loop """
for x in range(1,10):
	print(x)
	
"""
	The range starts with 1 and goes until the 10th element 
	
	1
	2
	3
	4
	5
	6
	7
	8
	9

"""
