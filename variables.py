""" Variable Tutorials """
"""
   Variables are nothing but reserved memory locations to store values. 
   This means that when you create a variable you reserve some space in memory
"""


"""
	Basic Variable:
	- one -> Variable Name
	- 1   -> Variable Value
"""
one = 1
print(one) 
""" Output : 1 """


"""
	Boolean Variable:
	- true_variable -> Variable Name
	- True 			-> Variable Value
	Note:
	- Value of boolean variable should be (True Or False) Not (true or false)
"""
true_variable = True
print(true_variable)




